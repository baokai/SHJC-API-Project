    public class VersionBLL : VersionIBLL
    {
        private VersionService versionServiceService = new VersionService();

        #region 获取数据

        /// <summary>
        /// 获取页面显示列表数据
        /// </summary>
        /// <param name="pagination">分页参数</param>
        /// <param name="queryJson">查询参数</param>
        /// <returns></returns>
        public IEnumerable<VersionVo> GetPageList()
        {
            try
            {
                return versionServiceService.GetPageList();
            }
            catch (Exception ex)
            {
                if (ex is ExceptionEx)
                {
                    throw;
                }
                else
                {
                    throw ExceptionEx.ThrowBusinessException(ex);
                }
            }
        }
       
        #endregion    

    }
